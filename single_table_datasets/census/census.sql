CREATE TABLE census (
    age INT,
    workclass VARCHAR(64),
    education VARCHAR(64),
    education_num INT,
    marital_status VARCHAR(64),
    occupation VARCHAR(64),
    relationship VARCHAR(64),
    race VARCHAR(64),
    sex VARCHAR(64),
    capital_gain INT,
    capital_loss INT,
    hours_per_week INT,
    native_country VARCHAR(64)
);